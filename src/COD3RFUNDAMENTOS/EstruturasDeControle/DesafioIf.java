package COD3RFUNDAMENTOS.EstruturasDeControle;

public class DesafioIf {
    public static void main(String[] args) {

        double nota = 1.3;

        if (nota >= 9.0);{
            System.out.println("Quadro de honra!");
            System.out.println("Você é fera");
        }
    }
}

// Porque o sistema executou o código normalmente foi o ; no IF.
/* Em java, ; significa encerrar o codigo. Como aquela expressão seria sempre falsa
ele deixou de executar o if e executou normalmente o restante. Nao se termina bloco de controle com ;

 */